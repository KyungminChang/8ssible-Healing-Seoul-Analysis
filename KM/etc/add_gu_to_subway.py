def add_gu_columns(input_path: str, output_path: str | None = None):
    """subway CSV에 gu_code, gu_name 컬럼 추가."""
    gu_master_path = 'KM/2024noise/seoul_gu_master_202605051639.csv'

    # gu_master 로드 → gu_name: gu_code 딕셔너리
    gm = pd.read_csv(gu_master_path)
    gu_name_to_code = dict(zip(gm['gu_name'], gm['gu_code'].astype(str)))

    # subway 파일 로드
    df = pd.read_csv(input_path)

    # STATION_ID 4자리 문자열로 변환
    sid = df['STATION_ID'].astype(str).str.zfill(4)

    # 매핑 적용
    df['gu_name'] = sid.map(STATION_GU_MAP)
    df['gu_code'] = df['gu_name'].map(gu_name_to_code)

    # 저장
    save_path = output_path or input_path
    df.to_csv(save_path, index=False, encoding='utf-8-sig')

    total = len(df)
    mapped = df['gu_name'].notna().sum()
    unmapped = df['gu_name'].isna().sum()
    print(f"[완료] {save_path}")
    print(f"  총 행: {total:,}  |  매핑 성공: {mapped:,}  |  서울 외(None): {unmapped:,}")
    if unmapped > 0:
        out = df[df['gu_name'].isna()][['STATION_ID', 'LINE_NM', 'STATION,_NM']].drop_duplicates()
        print(f"  미매핑 역 (서울 외):\n{out.to_string(index=False)}")
    return df


